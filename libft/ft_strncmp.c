#include "libft.h"

int ft_strncmp(const char *s1, const char *s2, size_t n)
{
	int diff;
	int i;

	diff = 0;
	i = 0;
	while (!(diff = s1[i] - s2[i]) && s2[i] && (--n))
	{
		i++;
	}
	return (diff);
}
